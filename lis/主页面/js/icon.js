$('#modalExample, #hide, #hideT').click(function() {
  $('#modalExample').hide();
  $('#modal-wrapper').hide();
  $('#modalExample2').hide();
  $('#modal-wrap').hide();
});

$('#shown').click(function() {
  $('#modalExample').show();
  $('#modal-wrapper').show();
});
$('#showed').click(function() {
  $('#modalExample2').show();
  $('#modal-wrap').show();
});


